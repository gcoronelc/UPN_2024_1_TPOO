
package pe.edu.upn.mateapp.service;

public class MateService {
	
	
	public int sumar(int n1, int n2){
		// Variables
		int suma;
		// Proceso
		suma = n1 + n2;
		// Reporte
		return suma;
	}
	
	public int restar (int n1, int n2){
		//varibles
		int resta;
		//proceso
		resta = n1 - n2;
		//reporte
		return resta;
	}
			 
	
}
